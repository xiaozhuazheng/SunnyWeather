package com.azheng.sunnyweather.data.model;

public class CityModel {
    public String cityName;
    public String cityWeather;
    public String cityTmp;

    public String getCityName() {
        return cityName;
    }

    public void setCityName(String cityName) {
        this.cityName = cityName;
    }

    public String getCityWeather() {
        return cityWeather;
    }

    public void setCityWeather(String cityWeather) {
        this.cityWeather = cityWeather;
    }

    public String getCityTmp() {
        return cityTmp;
    }

    public void setCityTmp(String cityTmp) {
        this.cityTmp = cityTmp;
    }
}
